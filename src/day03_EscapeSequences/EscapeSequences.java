package day03_EscapeSequences;
/*
Escape Sequences : MUST be giving with double quote
\n :start the new line
\t: tab button
\\ : single slash
 */
public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Java \nPhyton \nC#");
        System.out.println("------------------------");
        System.out.println("Hello Cydeo \nHoe are you all today? \nit is nice to see you all.");
        System.out.println("--------------------------------");
        System.out.println("Java is cool Programming language.");
        System.out.println("\tJava is cool programming language.");
        System.out.println("/\\");
        System.out.println("----------------------------------------------");
        System.out.println("My favourite program is Game of Throne ");
        System.out.println("My favourite program is \"Game of Throne\"");
        System.out.println("-------------------------------------------------");




    }
}
